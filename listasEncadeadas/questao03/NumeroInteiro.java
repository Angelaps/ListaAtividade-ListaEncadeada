public class NumeroInteiro {

	private int numeros;
	
	public NumeroInteiro() {
		super();
	}

	public NumeroInteiro(int numeros) {
		this.numeros = numeros;
	}

	public int getNumeros() {
		return numeros;
	}

	public void setNumeros(int numeros) {
		this.numeros = numeros;
	}
}